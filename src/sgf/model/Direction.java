package sgf.model;
/**
 * 
 * This enumeration represents a simple direction, that will be useful in order to simplify
 * enemies' movement along the path.
 *
 */
public enum Direction {
    /**
     * Left direction.
     */
    LEFT,
    /**
     * Right direction.
     */
    RIGHT,
    /**
     * Up direction.
     */
    UP,
    /**
     * Down direction.
     */
    DOWN
}
